void main() {
  String trafficLightColor = "green"; // The traffic light's color

  // Conditional statement: The traffic light tells you what to do.
  if (trafficLightColor == "red") {
    print("Stop the car!");
  } else if (trafficLightColor == "yellow") {
    print("Slow down and prepare to stop.");
  } else {
    print("Go ahead. The road is clear.");
  }
}
